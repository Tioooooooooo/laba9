# Создаем словарь: ключ - страна, значение - столица
countries_capitals = {
    "Россия": "Москва",
    "Франция": "Париж",
    "Япония": "Токио",
    "Италия": "Рим"
}

print("--- Пункт a: Все пары ключ-значение ---")
for country, capital in countries_capitals.items():
    print(f"Страна: {country}, Столица: {capital}")

print("\n--- Пункт b: Столица для определенной страны ---")
# Получаем значение по ключу (например, для Франции)
target_country = "Франция"
print(f"Столица страны '{target_country}' — {countries_capitals[target_country]}")

print("\n--- Пункт c: Отсортированный вывод ---")
# Функция sorted() сортирует ключи (названия стран) по алфавиту
for country in sorted(countries_capitals):
    print(f"{country} - {countries_capitals[country]}")

