# Словарь стоимости букв
letter_scores = {
    'А': 1, 'В': 1, 'Е': 1, 'И': 1, 'Н': 1, 'О': 1, 'Р': 1, 'С': 1, 'Т': 1,
    'Д': 2, 'К': 2, 'Л': 2, 'М': 2, 'П': 2, 'У': 2,
    'Б': 3, 'Г': 3, 'Ё': 3, 'Ь': 3, 'Я': 3,
    'Й': 4, 'Ы': 4,
    'Ж': 5, 'З': 5, 'Х': 5, 'Ц': 5, 'Ч': 5,
    'Ш': 8, 'Э': 8, 'Ю': 8,
    'Ф': 10, 'Щ': 10, 'Ъ': 10
}

# Получаем слово от пользователя и сразу делаем все буквы заглавными
word = input("Введите слово: ").upper()

total_score = 0
# Перебираем каждую букву в слове
for letter in word:
    # Если буква есть в нашем словаре (чтобы избежать ошибки из-за пробелов или цифр)
    if letter in letter_scores:
        total_score += letter_scores[letter] # Добавляем очки за эту букву

print(f"Стоимость слова '{word}': {total_score} очков")


# задание 3
# Словарь студентов, где значения - это множества (set) языков
students = {
    "Иванов": {"английский", "испанский"},
    "Петров": {"китайский", "русский"},
    "Сидоров": {"английский", "китайский", "французский"},
    "Смирнов": {"русский", "испанский"}
}

# 1. Считаем, сколько различных языков знают студенты
all_languages = set() # Создаем пустое множество для всех языков

# Перебираем множества языков каждого студента
for languages in students.values():
    # Объединяем наше общее множество с множеством конкретного студента
    all_languages = all_languages | languages

print(f"Всего студенты знают {len(all_languages)} различных языков.")

# 2. Выводим отсортированный список этих языков
# Функция sorted() превращает множество в отсортированный список (list)
print("Отсортированный список языков:", sorted(all_languages))

# 3. Выводим список студентов, которые знают китайский
chinese_speakers = []
for student_name, languages in students.items():
    # Проверяем, есть ли 'китайский' в множестве языков этого студента
    if "китайский" in languages:
        chinese_speakers.append(student_name)

print("Студенты, которые знают китайский:", chinese_speakers)